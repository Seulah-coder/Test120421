package second_pkg;

// class이름 작성시 영어로 작성
// 시작글자는 대문자(ex. Computer)
// 두 단어 이상의 조합으로 클래스 이름을 작성하는 경우 
// ComputerDesk(시작 글자를 대문자로) or Computer_Class, Computer-Class but Computer Class는 불가

public class SecondClass {
	// 코드를 작성 후 실행을 하면
	// jdk에 의해 컴파일이 진행되면서 SecondClass.class라는 파일이 만들어지고
	// jre가 이.class 파일을 실행하여 console 창에 결과를 보여줌
	
	public static void main(String[] args) {
		System.out.println("Hello World!!");
		System.out.println("The text here will be shown in console.");
		System.out.print("print");
		System.out.println("println");
		System.out.println("next line of println");
		
		// .의 의미
		// .앞에 작성한 요소(클래스)에서 제공하는 메소드 또는 변수 등을 쓰고자 할때 사용하는 문법 

	}

}
